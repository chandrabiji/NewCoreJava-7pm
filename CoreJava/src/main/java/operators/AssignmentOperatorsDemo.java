package operators;
// = , += , -=, *=, /=, %=
public class AssignmentOperatorsDemo {

	public static void main(String[] args) {
		int a = 4;
		System.out.println(" = "+a);
		a +=2;//a = a +2 = 4+2=6
		System.out.println(" += "+a);
		a -=2;//a = a-2 = 6-2=4
		System.out.println(" -= "+a);
		a *=2;//a = a*2=4 * 2 = 8
		System.out.println(" *= "+a);
		a /=2;
		System.out.println(" /= "+a);
		a %= 2;
		System.out.println(" %= "+a);
		

	}

}
